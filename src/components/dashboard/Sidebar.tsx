export default function Sidebar({ isOpen, onClose }: { isOpen: boolean; onClose: () => void }) {
    const pathname = usePathname();
    const [expandedItems, setExpandedItems] = useState<string[]>([]);

    const toggleExpand = (title: string) => {
        setExpandedItems(prev =>
            prev.includes(title)
                ? prev.filter(item => item !== title)
                : [...prev, title]
        );
    };

    return (
        <>
            {/* Mobile Overlay */}
            {isOpen && (
                <div
                    className="fixed inset-0 bg-black/50 z-40 lg:hidden"
                    onClick={onClose}
                />
            )}

            {/* Sidebar */}
            <aside className={`
                fixed top-0 left-0 z-50 h-screen w-64 bg-[#06124f] text-white
                transform transition-transform duration-300 ease-in-out
                lg:translate-x-0 lg:static lg:z-auto
                ${isOpen ? 'translate-x-0' : '-translate-x-full'}
            `}>
                {/* Logo */}
                <div className="flex items-center justify-between p-6 border-b border-white/10">
                    <Link href="/dashboard" className="flex items-center space-x-3">
                        <Image 
                            src="/logo.png" 
                            alt="VIROS Logo" 
                            width={40} 
                            height={40}
                            className="object-contain bg-white p-1 rounded-md"
                        />
                        <span className="font-bold text-lg"> Viros Admin</span>
                    </Link>

                    {/* Close button for mobile */}
                    <button
                        onClick={onClose}
                        className="lg:hidden text-white/60 hover:text-white"
                    >
                        <svg className="w-6 h-6" fill="none" stroke="currentColor" viewBox="0 0 24 24">
                            <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M6 18L18 6M6 6l12 12" />
                        </svg>
                    </button>
                </div>

                {/* Navigation */}
                <nav className="p-4 space-y-1 overflow-y-auto h-[calc(100vh-80px)]">
                    {menuItems.map((item) => {
                        const isExpanded = expandedItems.includes(item.title);
                        const hasSubItems = 'subItems' in item && item.subItems;
                        
                        return (
                            <div key={item.title}>
                                {/* Main Item */}
                                {hasSubItems ? (
                                    <button
                                        onClick={() => toggleExpand(item.title)}
                                        className="w-full flex items-center justify-between px-4 py-3 rounded-lg
                                            text-white/70 hover:bg-white/10 hover:text-white
                                            transition-all duration-200"
                                    >
                                        <div className="flex items-center space-x-3">
                                            {item.icon}
                                            <span className="font-medium">{item.title}</span>
                                        </div>
                                        <svg 
                                            className={`w-4 h-4 transition-transform duration-200 ${isExpanded ? 'rotate-180' : ''}`}
                                            fill="none" 
                                            stroke="currentColor" 
                                            viewBox="0 0 24 24"
                                        >
                                            <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M19 9l-7 7-7-7" />
                                        </svg>
                                    </button>
                                ) : (
                                    <Link
                                        href={item.href!}
                                        onClick={onClose}
                                        className={`
                                            flex items-center space-x-3 px-4 py-3 rounded-lg
                                            transition-all duration-200
                                            ${pathname === item.href
                                                ? 'bg-[#06b6d4] text-white shadow-lg'
                                                : 'text-white/70 hover:bg-white/10 hover:text-white'
                                            }
                                        `}
                                    >
                                        {item.icon}
                                        <span className="font-medium">{item.title}</span>
                                    </Link>
                                )}

                                {/* Sub Items */}
                                {hasSubItems && isExpanded && (
                                    <div className="ml-4 mt-1 space-y-1">
                                        {item.subItems.map((subItem) => {
                                            const isActive = pathname === subItem.href;
                                            return (
                                                <Link
                                                    key={subItem.href}
                                                    href={subItem.href}
                                                    onClick={onClose}
                                                    className={`
                                                        flex items-center space-x-3 px-4 py-2 rounded-lg
                                                        transition-all duration-200
                                                        ${isActive
                                                            ? 'bg-[#06b6d4] text-white shadow-lg'
                                                            : 'text-white/60 hover:bg-white/10 hover:text-white'
                                                        }
                                                    `}
                                                >
                                                    {subItem.icon}
                                                    <span className="text-sm">{subItem.title}</span>
                                                </Link>
                                            );
                                        })}
                                    </div>
                                )}
                            </div>
                        );
                    })}
                </nav>
            </aside>
        </>
    );
}
